export function packExplosionSources(sources: readonly ExplosionSource[]) {
  const floatsPerSource = 32
  const data = new Float32Array(sources.length * floatsPerSource)

  sources.forEach((source, index) => {
    const offset = index * floatsPerSource

    data.set([...source.position, source.radius], offset)
    data.set([
      source.startTime,
      source.smokeLeadTime,
      source.blastDuration,
      source.plumeDuration,
    ], offset + 4)
    data.set([
      source.densityYield,
      source.heatYield,
      source.fuelYield,
      source.reactionYield,
    ], offset + 8)
    data.set([
      source.radialImpulse,
      source.turbulence,
      source.crumbleStrength,
      source.implosionStrength ?? 0,
    ], offset + 12)
    data.set([...source.liftDirection, source.liftImpulse], offset + 16)
    data.set([
      source.patchScale,
      source.coreHeat,
      source.coreLift,
      source.heatPatchiness,
    ], offset + 20)
    data.set([
      source.seed,
      source.expansionRate ?? 1,
      source.sustain ?? 0,
      source.mushroomStrength ?? 1,
    ], offset + 24)
    data.set([source.smokeEntrainment ?? 1, 0, 0, 0], offset + 28)
  })

  return data
}

